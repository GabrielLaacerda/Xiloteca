
function setaVasos() {
    var html = $("#tableVasos");
    var html1 = $("#tableEspecie");
    var html2 = $("#tableParenquima");
    var html3 = $("#tableOrganolepticas");
    var html4 = $("#tableCaracEspeciais");
    var html5 = $("#tableRaios");

    html1.addClass("d-none");
    html2.addClass("d-none");
    html3.addClass("d-none");
    html4.addClass("d-none");
    html5.addClass("d-none");

    html.removeClass("d-none");

}
